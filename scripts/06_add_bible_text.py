def main():
    data = json.loads(QUIZZES.read_text(encoding="utf-8"))
    qs = data["quizzes"]
    added = 0
    for q in qs:
        ref = q.get("bible_ref", "").strip()
        # 정확 매칭 또는 prefix 매칭
        text = BIBLE_KOR.get(ref)
        if not text:
            # prefix만 매칭 (예: "엡 2:8-9" → "엡 2:8")
            for k, v in BIBLE_KOR.items():
                if ref.startswith(k):
                    text = v
                    break
        if text:
            q["bible_text"] = text
            added += 1
    QUIZZES.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"✅ bible_text 추가: {added}/{len(qs)}")

if __name__ == "__main__":
    main()
